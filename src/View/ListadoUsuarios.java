    private void cargarDatos() {
        ModeloTabla.setRowCount(0); // Limpia la tabla
        List<Usuarios> lista = usuariosDAO.getAllUsuarios();
        for (Usuarios u : lista) {
            ModeloTabla.addRow(new Object[]{
                u.getId(),
                u.getNombre(),
                u.getCorreo(),
                u.getTipoUsuarioId()  
            });
        }
    }

    public void refrescarInfo() {
        cargarDatos();
    }


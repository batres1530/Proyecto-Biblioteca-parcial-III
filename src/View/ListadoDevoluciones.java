    private void cargarDatos() {
        ModeloTabla.setRowCount(0); // Limpia la tabla
        List<Devoluciones> devoluciones = devolucionDAO.getAll();
        for (Object obj : devoluciones) {
            Devoluciones devolucion = (Devoluciones) obj;
            ModeloTabla.addRow(new Object[]{
                devolucion.getDevolucionesId(),
                devolucion.getPrestamoId(),
                devolucion.getFechaDevolucion(),
                devolucion.getMulta()
            });
        }
    }
    
    public void refrescarInfo() {
        cargarDatos();
    }


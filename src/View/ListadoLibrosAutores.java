    private void cargarDatos() {
        modeloTabla.setRowCount(0); // Limpiar tabla
        List<LibrosAutores> lista = dao.getAllConNombres(); // JOIN con nombre de autor y libro
        for (LibrosAutores la : lista) {
            modeloTabla.addRow(new Object[]{
                la.getLibroAutorID(),
                la.getTituloLibro(),
                la.getNombreAutor()
            });
        }
    }

    public void refrescarInfo() {
        cargarDatos();
    }

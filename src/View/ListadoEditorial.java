    private void cargarDatos() {
        ModeloTabla.setRowCount(0); // Limpia la tabla
        List<Editorial> lista = editorialDAO.getAllEditoriales2();
        for (Editorial ed : lista) {
            ModeloTabla.addRow(new Object[]{
                ed.getId(),
                ed.getNombre(),
                ed.getDireccion(),
                ed.getTelefono()
            });
        }
    }

    public void refrescarInfo() {
        cargarDatos();
    }


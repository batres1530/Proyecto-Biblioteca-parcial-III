    private void btnentrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnentrarActionPerformed
      String username = txtNombre.getText().trim();
        String password = new String(txtContrasena.getPassword()).trim(); 

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, llena todos los campos.");
            return;
        }

        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/BDBiblioteca", "SDBM15", "Davidmartinez1530.")) {
            String sql = "SELECT * FROM usuario WHERE username = ?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, username);
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                String hashAlmacenado = rs.getString("password_hash");
                String hashIngresado = encriptarSHA256(password);

                if (hashAlmacenado.equals(hashIngresado)) {
                    JOptionPane.showMessageDialog(this, "Bienvenido " + username);
                    new FormularioMenu().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Contrase√±a incorrecta.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Usuario no encontrado.");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al conectar con la base de datos.");
        }
    }//GEN-LAST:event_btnentrarActionPerformed


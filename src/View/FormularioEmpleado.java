    private ListadoEmpleado parent;
    
    public FormularioEmpleado(ListadoEmpleado pParent, boolean modal, String pMode, int pId) {
        super(pParent, modal);
        this.parent = pParent;
        this.Mode = pMode;
        this.id = pId;
        initComponents();
        
        cmbCargo = new JComboBox<>();
        cmbCargo.setBounds(200, 200, 200, 25); // ajusta posición/tamaño según tu diseño
        add(cmbCargo);
        cargarCargos();
        
        txt_id.setText(String.valueOf(pId));
        txt_id.setEnabled(false);

        if (Mode.equals("INS")) {
            btn_accion.setText("Insertar");
        } else {
            Empleados empleado = dao.buscarPorId(id);
            if (empleado != null) {
                txt_id.setText(String.valueOf(empleado.getEmpleadoID()));
                txt_nombre.setText(empleado.getNombre());
                txt_apellido.setText(empleado.getApellido());
                txt_telefono.setText(empleado.getTelefono());

                // Seleccionar en el combo el cargo correspondiente
                for (int i = 0; i < cmbCargo.getItemCount(); i++) {
                    CargoComboItem item = cmbCargo.getItemAt(i);
                    if (item.getId() == empleado.getCargosID()) {
                        cmbCargo.setSelectedIndex(i);
                        break;
                    }
                }

                if (Mode.equals("UPD")) {
                    btn_accion.setText("Actualizar");
                } else if (Mode.equals("DLT")) {
                    txt_nombre.setEditable(false);
                    txt_apellido.setEditable(false);
                    txt_telefono.setEditable(false);
                    cmbCargo.setEnabled(false);
                    btn_accion.setText("Eliminar");
                }
            }
        }
    }
    
    private void cargarCargos() {
    List<CargoComboItem> cargos = dao.obtenerCargosParaCombo();
    cmbCargo.removeAllItems();

    for (CargoComboItem item : cargos) {
        cmbCargo.addItem(item);
    }
}

    

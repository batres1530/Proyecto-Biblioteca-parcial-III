    private void cargarDatos() {
        ModeloTabla.setRowCount(0); // Limpia la tabla
        List<Ejemplares> lista = ejemplaresDAO.getAllEjemplares();
        for (Ejemplares ej : lista) {
            ModeloTabla.addRow(new Object[]{
                ej.getEjemplarID(),
                ej.getTituloLibro(),
                ej.getEstado()
            });
        }
    }

    public void refrescarInfo() {
        cargarDatos();
    }


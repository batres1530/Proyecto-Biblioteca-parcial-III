    private void btn_accionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_accionActionPerformed
        // TODO add your handling code here:
        if (Mode.equals("INS")) {
            try {
                String nombre = txt_nombre.getText().trim();
                String apellido = txt_apellido.getText().trim();
                String telefono = txt_telefono.getText().trim();
                CargoComboItem cargoSeleccionado = (CargoComboItem) cmbCargo.getSelectedItem();

                if (nombre.isEmpty() || apellido.isEmpty() || telefono.isEmpty() || cargoSeleccionado == null) {
                    JOptionPane.showMessageDialog(this, "Debe ingresar nombre, apellido y seleccionar un cargo.");
                    return;
                }

                Empleados nuevoEmpleado = new Empleados();
                nuevoEmpleado.setNombre(nombre);
                nuevoEmpleado.setApellido(apellido);
                nuevoEmpleado.setTelefono(telefono);
                nuevoEmpleado.setCargosID(cargoSeleccionado.getId());

                if (dao.insert(nuevoEmpleado)) {
                    JOptionPane.showMessageDialog(this, "Empleado insertado correctamente.");
                    parent.refrescarInfo();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "No se pudo insertar el empleado.");
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error al insertar: " + e.getMessage());
            }
        }

        if (Mode.equals("UPD")) {
            try {
                String nombre = txt_nombre.getText().trim();
                String apellido = txt_apellido.getText().trim();
                String telefono = txt_telefono.getText().trim();
                CargoComboItem cargoSeleccionado = (CargoComboItem) cmbCargo.getSelectedItem();

                if (nombre.isEmpty() || apellido.isEmpty() || telefono.isEmpty() || cargoSeleccionado == null) {
                    JOptionPane.showMessageDialog(this, "Debe ingresar nombre, apellido y seleccionar un cargo.");
                    return;
                }

                Empleados empleadoActualizado = new Empleados();
                empleadoActualizado.setEmpleadoID(id);
                empleadoActualizado.setNombre(nombre);
                empleadoActualizado.setApellido(apellido);
                empleadoActualizado.setTelefono(telefono);
                empleadoActualizado.setCargosID(cargoSeleccionado.getId());

                if (dao.update(empleadoActualizado)) {
                    JOptionPane.showMessageDialog(this, "Empleado actualizado correctamente.");
                    parent.refrescarInfo();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "No se pudo actualizar el empleado.");
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error al actualizar: " + e.getMessage());
            }
        }

        if (Mode.equals("DLT")) {
            int confirmacion = JOptionPane.showConfirmDialog(this, "¿Deseas eliminar este empleado?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

            if (confirmacion == JOptionPane.YES_OPTION) {
                boolean eliminado = dao.delete(id);

                if (eliminado) {
                    JOptionPane.showMessageDialog(this, "Empleado eliminado correctamente.");
                    parent.refrescarInfo();
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "No se pudo eliminar el empleado.");
                }
            }
        }
    }//GEN-LAST:event_btn_accionActionPerformed

